# tuple operations: elements enclosed in small parenthesis() tuple
# tuples are imutable i.e we cant change them
t1 = ('a','bc',78,1.23)
t2 = ('d',78)
print(t1)   
print(t2)  
print(t1[0]) 
print(t1[1:3])
print(t1[2:])
print(t1[-1])
print(t1*2)
print(t1+t2)
t1[2]=80  # cannot be reassigned







