# loop : a loop allows to execute set of statements repeatedly desired
# no of times based on a conditon.
# loop will be continued as long as the condition is true
# loop will be terminated when the condition is false
# there 3 types of loops 1. while 2. do while 3. for
# program to print numbers from 1 to a given number

i=1

n=int(input("enter a number"))

while(i<=n):
    print(i)
    i=i+1
