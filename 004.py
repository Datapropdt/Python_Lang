# string slicing operations(:)
str = "python is easy"
print(str)
print(str[0]) #print 0 th letter
print(str[3:9]) # print 3 to 8 letter
print(str[4:]) # print from 4 to last
print(str[-2]) # print last letter
print(str[:5]) # print first 5 letters
print(str*2) # print string 2 times
print(str+" isnt it") #combining string variable with text





